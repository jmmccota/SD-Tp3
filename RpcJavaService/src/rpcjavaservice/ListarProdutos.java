    void atualiza() {
        int totalLinhas = modelTable.getRowCount();//pega numero total de linhas
        for (int i = 0; i < totalLinhas; i++) {//percorre o vetor
            modelTable.removeRow(0);//apaga as linhas
        }
        ArrayList<Produto> pList = aLeiloeiro.arrayProdutos;
        for (int i = 0; i < pList.size(); i++) {
            long aux = new Date().getTime();
            if (aux <= pList.get(i).getTermino()) {
                modelTable.addRow(new Object[]{i, pList.get(i).getNome(), pList.get(i).getPreco(), pList.get(i).getCliente(), (pList.get(i).getTermino() * 0.001),aux});
            }
        }
    }

